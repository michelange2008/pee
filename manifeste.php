      <h6 class="center" style="font-weight:bold">Plantes en élevage : manifeste des 1053 éleveurs et éleveuses hors-la-loi</h6>
      <p class="justify">
        Nous, éleveurs-euses bovins, caprins, ovins, porcins, équins, de volailles, apiculteurs-trices ;
Que nous soyons en agriculture biologique ou en conventionnel, en montagne ou en plaine, nous
utilisons des plantes pour prévenir les maladies et pour les soins à nos animaux.</p>
<p style="font-weight:bold">Nous privilégions l’usage des plantes, plutôt que des antibiotiques ou tout autre produit chimique de
synthèse.</p><p style="font-weight:bold">
Et nous sommes dans l’illégalité !</p><p>
En valorisant un savoir-faire traditionnel basé sur la nature, en protégeant ainsi nos animaux et nos
concitoyens-nes de l’antibiorésistance, en préservant l’eau de contaminations par des médicaments
chimiques de synthèse, nous sommes hors-la-loi.</p><p>
L’usage généralisé des antibiotiques pose aujourd’hui un problème de santé publique. Chaque
année, en France, plus de 150 000 patients développent une infection liée à une bactérie multirésistante, et plus de 12 500 personnes en meurent.<p></p>
Face à cette urgence, il est incohérent que l’usage des plantes en élevage se voit imposer un tel
carcan réglementaire.</p><p>
En effet, aujourd’hui, pour pouvoir utiliser des plantes en élevage, elles doivent disposer d’une
autorisation de mise sur le marché (AMM), comme les médicaments, et être prescrites par un
vétérinaire. Or, très peu de médicaments à base de plantes disposent de cette AMM, procédure
lourde et inadaptée. Au mieux, nous avons le droit d’utiliser des préparations sur prescription d’un
vétérinaire, lorsqu’aucun autre médicament n’est disponible !</p><p>
Impossible donc, pour nous, éleveurs et éleveuses, d’utiliser des orties, du romarin, du pissenlit, de la
lavande… pour les soins à nos troupeaux. La loi nous contraint à leur préférer des produits
antibiotiques, anti-inflammatoires ou anti-parasitaires issus de la chimie de synthèse !<p></p>
Parce que nous voulons continuer à nous former.</p><p>
Parce que nous voulons expérimenter, échanger et débattre librement sur ces pratiques avec nos
collègues.<p></p>
Parce que nous ne voulons plus être hors-la-loi !</p><p>
L’Etat doit en urgence définir un cadre réglementaire spécifique pour pouvoir utiliser les plantes en
élevage et mettre un terme à cette situation.
